BilanganPertama = float(input("Masukkan bilangan pertama: "))
BilanganKedua = float(input("Masukkan bilangan kedua: "))
BilanganKetiga = float(input("Masukkan bilangan ketiga: "))

BilanganKecil = min(BilanganPertama, BilanganKedua, BilanganKetiga)

print("Bilangan terkecil adalah: %.0f"%BilanganKecil)

